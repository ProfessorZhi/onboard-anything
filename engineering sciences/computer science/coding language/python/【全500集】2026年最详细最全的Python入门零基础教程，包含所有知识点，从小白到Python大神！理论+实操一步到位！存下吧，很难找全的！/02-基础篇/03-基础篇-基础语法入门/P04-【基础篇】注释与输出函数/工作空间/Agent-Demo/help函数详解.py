# ============================================================
# help() 函数完整演示
# help 是 Python 内置函数，不需要安装，直接用
# ============================================================

# -----------------------------------------------------------
# help 是什么？
# -----------------------------------------------------------
# help() 是 Python 的交互式帮助系统
# 传入一个对象（函数、类、模块、变量等），它会告诉你这东西是什么、怎么用
# 等价于在 Python 交互式解释器里直接敲 help()

# -----------------------------------------------------------
# 1. help(函数) — 查看函数的说明
# -----------------------------------------------------------
print("=== 1. help(函数) ===")
help(print)          # 打印 print 的参数列表和用法说明
# 退出帮助界面后程序继续运行

print("\n" + "=" * 50 + "\n")

# -----------------------------------------------------------
# 2. help(类) — 查看类的说明和所有方法
# -----------------------------------------------------------
print("=== 2. help(类) ===")
help(str)            # 查看字符串类的所有方法和属性

print("\n" + "=" * 50 + "\n")

# -----------------------------------------------------------
# 3. help(模块) — 查看模块里有什么
# -----------------------------------------------------------
print("=== 3. help(模块) ===")
import os
help(os)             # 查看 os 模块的完整说明（很长）

print("\n" + "=" * 50 + "\n")

# -----------------------------------------------------------
# 4. help(变量/对象) — 查看具体对象的类型和值
# -----------------------------------------------------------
print("=== 4. help(变量) ===")
x = [1, 2, 3]
help(x)              # 列出列表的所有操作方法

print("\n" + "=" * 50 + "\n")

# -----------------------------------------------------------
# 5. help("关键词") — 查看关键词或语法的说明
# -----------------------------------------------------------
print("=== 5. help('关键词') ===")
help("if")           # 查看 if 语句的说明
help("class")        # 查看 class 的说明

print("\n" + "=" * 50 + "\n")

# -----------------------------------------------------------
# 6. 直接 help() — 进入交互式帮助界面（会阻塞等待输入）
# -----------------------------------------------------------
# help()
# 输入想查询的内容，比如 "print"
# 输入 q 退出帮助界面

# -----------------------------------------------------------
# 7. help(函数.__doc__) — 直接访问文档字符串
# -----------------------------------------------------------
print("=== 7. 直接访问 __doc__ ===")
def add(a, b):
    """计算两个数的和并返回结果"""
    return a + b

print(add.__doc__)                   # 打印函数的文档字符串
print()

# -----------------------------------------------------------
# 8. help 的替代方案：__doc__ + inspect
# -----------------------------------------------------------
print("=== 8. 用 __doc__ 和 inspect 替代 help ===")
import inspect

print("8a. 函数文档字符串：")
print(f"   add.__doc__ = {add.__doc__}")

print()
print("8b. 用 inspect.getdoc 获得格式化文档：")
print(f"   inspect.getdoc(add) = {inspect.getdoc(add)}")

print()
print("8c. 用 inspect.signature 获得函数签名：")
sig = inspect.signature(add)
print(f"   inspect.signature(add) = {sig}")
print(f"   参数: {list(sig.parameters.keys())}")
print(f"   返回值: {sig.return_annotation}")

print()
print("8d. 用 inspect.getsource 获得源代码：")
# print(inspect.getsource(add))  # 如果在文件里可以获取源码

print()
print("8e. 用 inspect.getfile 获得文件路径：")
print(f"   inspect.getfile(add) = {inspect.getfile(add)}")

# -----------------------------------------------------------
# 9. dir() — 列出对象所有属性/方法（配合 help 用）
# -----------------------------------------------------------
print()
print("=== 9. dir() 列出对象所有属性 ===")
print("   dir(str) =", dir(str))
print()
print("   过滤出只以下划线开头的方法：")
print("   [x for x in dir(str) if x.startswith('_')] =", [x for x in dir(str) if x.startswith('_')][:10], "...")

# -----------------------------------------------------------
# 10. 常用场景：查数据类型
# -----------------------------------------------------------
print()
print("=== 10. type() + help 组合查类型 ===")
data = {"name": "小明", "age": 25}
print(f"   data = {data}")
print(f"   type(data) = {type(data)}")
print(f"   type(data).__name__ = {type(data).__name__}")
print(f"   type(data).__doc__ = {type(data).__doc__}")
