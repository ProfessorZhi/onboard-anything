# -----------------------------------------------------------
# 第1种：单行注释 — # 开头，到行尾结束
# -----------------------------------------------------------
# 这是单行注释，解释下一行代码的作用
age = 18  # 行内注释，解释变量含义

# -----------------------------------------------------------
# 第2种：多行字符串当作注释（不被解释器处理的"假注释"）
# -----------------------------------------------------------
"""这是多行字符串
通常用于 docstring（函数/类的说明文档）
但也可以当作多行注释用
（实际上会被存储为字符串，不影响程序运行）
"""

'''单引号版本
同样是多行字符串
'''

